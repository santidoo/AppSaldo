	static private String getCodeByName(String name) {

		// Vodafone
		if (name.toLowerCase().contains("vodafone"))
			return "*141#";

		// BSNL India | CellOne | Dial Customer care at 1503 from BSNL mobile
		else if (name.toLowerCase().contains("bsnl") || name.toLowerCase().contains("cellone"))
			return "*123#";
		// Check URL for more codes down

		// Bharti Airtel India
		else if (name.toLowerCase().contains("airtel") || name.toLowerCase().contains("bharti"))
			return "*123#";

		// Videocon | "s121:BAL"
		else if (name.toLowerCase().contains("videocon"))
			return "*123*1#";

		// Reliance GSM | s53670:BAL
		else if (name.toLowerCase().contains("reliance gsm"))
			return "*367#";
		
		// Reliance CDMA | "s53670:BAL" , "d*369" , "d*225" (you will receive a msg)
		else if (name.toLowerCase().contains("reliance cdma"))
			return "*225";
		
		// dial *225/*226 (CDMA) or *367# (GSM) from your Reliance mobile. These numbers are toll-free
		// Reliance | Tata Docomo
		else if (name.toLowerCase().contains("reliance"))
			return "s55333:BAL";

		// Idea India | Idea Bta Cellcom | *212#
		// Escorts Telecommunications Limited (IDEA)
		else if (name.toLowerCase().contains("!dea") || name.toLowerCase().contains("idea") || name.toLowerCase().contains("bta") || 
				 name.toLowerCase().contains("cellcom") || name.toLowerCase().contains("escorts"))
			return "*130#";

		// TalkTime Orange GSM
		else if (name.toLowerCase().contains("talktime") || name.toLowerCase().contains("orange"))
			return "*111#";

		// TATA DOCOMO / Tata Indicom / Tata Docomo CDMA | *111#
		else if (name.toLowerCase().contains("tata") || name.toLowerCase().contains("docomo") || name.toLowerCase().contains("indicom"))
			return "s121:BAL";

		// T24
		else if (name.toLowerCase().contains("t24"))
			return "*111#";

		// Loop India
		else if (name.toLowerCase().contains("loop") || name.toLowerCase().contains("bpl"))
			return "*100#";

		// MTNL India
		else if (name.toLowerCase().contains("mtnl"))
			return "*444#";
		
		// DOLPHIN
		else if (name.toLowerCase().contains("dolphin"))
			return "*444#";

		// Uninor India | Dial 123 OR *363*4#
		else if (name.toLowerCase().contains("uninor") || name.toLowerCase().contains("telenor"))
			return "*222*2#";

		// Aircell Digilink India Ltd.
		else if (name.toLowerCase().contains("aircel") || name.toLowerCase().contains("digilink"))
			return "*125#";

		// S Tel
		else if (name.toLowerCase().contains("s tel"))
			return "444";

		// Virgin Mobile | "d125555"
		else if (name.toLowerCase().contains("virgin"))
			return "s58576:BA";

		// Jio 4G | Prepaid: BAL to 199
		else if (name.toLowerCase().contains("jio"))
			return "s199:BAL";

		else
			return "";
	}
